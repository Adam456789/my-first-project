const story = document.getElementById("story");
const analyzeBtn = document.getElementById("analyzeBtn");
const resultText = document.getElementById("resultText");

analyzeBtn.addEventListener("click", () => {
  const text = story.value.trim();

  if (!text) {
    resultText.textContent = "你还没有输入情况。先写清楚：你们是什么关系、对方说了什么、你想达到什么效果。";
    return;
  }

  let result = "";

  if (text.includes("分手") || text.includes("前任") || text.includes("复合")) {
    result = `关系判断：
这更像是前任关系里的试探。对方联系你，不一定代表想复合，也可能只是怀旧、确认你是否还在意，或者想找情绪价值。

风险提醒：
不要一上来就问“你是不是想复合”。这样容易让你失去主动权，也会让聊天变重。

推荐回复：
“最近还可以，谢谢关心。你呢？”

为什么这样回：
这句话礼貌、稳定、不暴露太多情绪，也给对方留下继续表达的空间。

下一步建议：
先看对方下一句。如果对方认真展开，可以慢慢聊；如果只是敷衍，就不要主动加码。`;
  } else if (text.includes("暧昧") || text.includes("喜欢") || text.includes("心动")) {
    result = `关系判断：
这更像是暧昧关系里的不确定阶段。你需要观察对方是持续投入，还是只在无聊时找你。

风险提醒：
不要太快把关系定义得很重，也不要只靠一句话判断对方喜不喜欢你。

推荐回复：
“哈哈，那你这是在关心我吗？”

为什么这样回：
这句话轻松、有试探空间，不会太卑微，也不会显得太急。

下一步建议：
观察对方是否继续接话、是否主动找话题、是否愿意付出时间。`;
  } else if (text.includes("冷淡") || text.includes("不回") || text.includes("已读不回")) {
    result = `关系判断：
对方冷淡可能代表忙、情绪低，也可能代表兴趣下降。重点不是猜，而是看对方后续有没有补偿性回应。

风险提醒：
不要连续发很多条，也不要用质问语气，比如“你为什么不回我”。

推荐回复：
“感觉你最近有点忙，那我先不打扰啦。”

为什么这样回：
这句话保留体面，也给对方一个主动解释或继续联系的机会。

下一步建议：
发完后先停下来。如果对方在意，会重新开启话题；如果一直没有回应，就把注意力收回到自己身上。`;
  } else if (text.includes("拉黑") || text.includes("删除") || text.includes("屏蔽")) {
    result = `关系判断：
拉黑、删除、屏蔽通常说明对方想拉开距离，或者关系里已经出现明显压力。

风险提醒：
不要换号联系、不要反复追问、不要通过朋友打听。这会让关系更紧张，也不利于你自己恢复。

推荐做法：
先停止主动联系，给彼此空间。

为什么这样做：
真正稳定的关系不靠追赶和逼问维持。先保护自己的情绪边界，比立刻要答案更重要。

下一步建议：
把注意力放回学习、生活和朋友身上。等情绪稳定后，再判断这段关系还值不值得继续。`;
  } else {
    result = `关系判断：
你描述的情况还不够具体，目前更适合先用稳妥、不暴露太多情绪的方式回复。

风险提醒：
不要急着表白、质问或连续追问。先让对话保持轻松。

推荐回复：
“我刚看到，你说这个是想问我什么呀？”

为什么这样回：
这句话能把问题抛回给对方，让对方表达真实意图。

下一步建议：
你可以补充：你们是什么关系、多久没联系、对方原话是什么。我再帮你判断得更准。`;
  }

  resultText.textContent = result;
});
